package Assignment1;


	/*2. Declare and initialize all 8 primitive datatypes, 
	store them in their wrapper class and print those as a Object.*/
public class Que_2 {
	    public static void main(String[] args) {
	        Byte b = 10;
	        Short s = 1000;
	        Integer i = 100000;
	        Long l = 10000000000L;
	        Float f = 3.14f;
	        Double d = 123.456;
	        Character c = 'A';
	        Boolean boo = true;

	        System.out.println(b);
	        System.out.println(s);
	        System.out.println(i);
	        System.out.println(l);
	        System.out.println(f);
	        System.out.println(d);
	        System.out.println(c);
	        System.out.println(boo);
	    }
	}


