package Assignment7;



public class Que_31 {

	    public static void main(String[] args) {
	        int num = 101;

	        while (true) {
	            if (num % 4 == 0 && num % 6 == 0) {
	                System.out.println("First number >100 divisible by 4 and 6: " + num);
	                break;
	            }
	            num++;
	        }
	    }
	}

	
	
